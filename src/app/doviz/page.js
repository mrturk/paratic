export default function Doviz() {
  const [data, setData] = useState([]);
  const [changedData, setChangedData] = useState({});
  const [searchValue, setSearchValue] = useState();

  const [favorite, setFavorite] = useLocalStorage(USER_FAV, []);

  const filterControl = (searchValue) => {
    if (searchValue) {
      const filteredItems = stabilData.filter((item) =>
        item.LegacyCode.toLowerCase().includes(searchValue?.toLowerCase())
      );
      setData(filteredItems);
    } else {
      setData(stabilData);
    }
  };

  const prepareData = useCallback(async () => {
    if (favorite.length <= 0 || favorite.length == undefined) {
      setData(stabilData);
    } else {
      stabilData = [...favorite];
      setData(favorite);
    }
  }, []);

  useEffect(() => {
    prepareData();
  }, []);

  useEffect(() => {
    socket.on("spot_pariteler", (res) => {
      if (res.data.Direction !== undefined) {
        setChangedData(res.data);
      }
    });
    return () => {
      window.location.reload();
    };
  }, [socket]);

  useEffect(() => {
    if (changedData) {
      stabilData.forEach((item) => {
        if (item._id === changedData.id) {
          item.Ask = changedData.Ask;
          item.Bid = changedData.Bid;
          item.DailyChange = changedData.DailyChange;
          item.DailyChangePercent = changedData.DailyChangePercent;
          item.DateTime = changedData.DateTime;
          item.Direction = changedData.Direction;
          item.Last = changedData.Last;
        }
      });

      filterControl(searchValue);
    }
  }, [changedData]);

  useEffect(() => {
    filterControl(searchValue);
  }, [searchValue]);

  return (
    <ParaticLayout>
      <ParaticTable
        onFavorite={(selectedItem) => {
          const newState = data.map((item) => {
            if (item._id === selectedItem._id) {
              return { ...item, IsFavorite: !item.IsFavorite };
            }
            return item;
          });
          stabilData = [
            ...stabilData.map((item) => {
              if (item._id === selectedItem._id) {
                return { ...item, IsFavorite: !item.IsFavorite };
              }
              return item;
            }),
          ];
          setFavorite(stabilData);
          setData(newState);
        }}
        data={data}
        setSearchValue={setSearchValue}
      />
    </ParaticLayout>
  );
}
